const mongoose = require('mongoose');
require('dotenv').config();

const LoginSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

const User = mongoose.model("users", LoginSchema);
module.exports = User;